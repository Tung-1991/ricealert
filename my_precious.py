def describe_market(ind):
    rsi = ind["rsi_14"]
    adx = ind["adx"]
    cmf = ind["cmf"]
    trend = ind["trend"]
    macd = ind["macd_cross"]

    if rsi > 65 and cmf > 0.05 and adx > 20 and trend == "uptrend":
        return "Th·ªã tr∆∞·ªùng ƒëang tƒÉng m·∫°nh, t√≠n hi·ªáu k·ªπ thu·∫≠t ƒë·ªìng thu·∫≠n"
    if 55 <= rsi <= 65 and cmf >= 0 and trend == "uptrend":
        return "Th·ªã tr∆∞·ªùng ƒëang tƒÉng nh·∫π v·ªõi h·ªó tr·ª£ t·ª´ d√≤ng ti·ªÅn"
    if 45 <= rsi <= 55 and adx < 15:
        return "Th·ªã tr∆∞·ªùng sideway, ch∆∞a r√µ xu h∆∞·ªõng"
